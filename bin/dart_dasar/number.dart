/**
 * 2 type of number variable in Dart
 * Int
 * Double
 */

void main() {
  int number1 = 10;
  double number2 = 10.5;

  print(number1);
  print(number2);

  // Num : Flexible number data type in Dart, can be int, can be double
  num number3 = 10;
  print(number3);

  number3 = 10.5;
  print(number3);

}